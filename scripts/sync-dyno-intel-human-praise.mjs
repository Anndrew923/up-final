#!/usr/bin/env node
/**
 * Syncs dynoIntel.json (humanBrief + humanPraise) → Functions runtime data layer.
 * Run: node scripts/sync-dyno-intel-human-praise.mjs
 *
 * DESIGN INTENT: i18n JSON is the single source of truth; Functions mirror is generated.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dynoIntelPath = join(root, "src/i18n/locales/zh-Hant/common/dynoIntel.json");
const praiseDataPath = join(root, "functions/dynoIntel/dynoIntelHumanPraise.data.js");
const scaleMatrixPath = join(root, "functions/dynoIntel/dynoIntelHumanScaleMatrix.js");

function loadPraiseSource() {
  const doc = JSON.parse(readFileSync(dynoIntelPath, "utf8"));
  const humanBrief = doc?.dynoIntel?.humanBrief;
  const humanPraise = doc?.dynoIntel?.humanPraise?.byDecade;
  const legalShield = doc?.dynoIntel?.hallOfFame?.legalShield;

  if (!humanBrief?.prPercentileFallback || !humanBrief?.hallOfFameSentence) {
    throw new Error("dynoIntel.json missing dynoIntel.humanBrief (prPercentileFallback + hallOfFameSentence)");
  }
  if (!legalShield || typeof legalShield !== "string") {
    throw new Error("dynoIntel.json missing dynoIntel.hallOfFame.legalShield");
  }
  if (!humanPraise || typeof humanPraise !== "object") {
    throw new Error("dynoIntel.json missing dynoIntel.humanPraise.byDecade");
  }

  return {
    prPercentileFallback: humanBrief.prPercentileFallback,
    hallOfFameSentence: humanBrief.hallOfFameSentence,
    hallOfFameLegalShield: legalShield,
    humanPraiseByDecade: humanPraise,
  };
}

function writePraiseDataModule({
  prPercentileFallback,
  hallOfFameSentence,
  hallOfFameLegalShield,
  humanPraiseByDecade,
}) {
  const body = `/**
 * AUTO-GENERATED by scripts/sync-dyno-intel-human-praise.mjs — do not edit by hand.
 * Source: src/i18n/locales/zh-Hant/common/dynoIntel.json (humanBrief + humanPraise + hallOfFame)
 */
export const DYNO_INTEL_PR_PERCENTILE_FALLBACK_ZH = ${JSON.stringify(prPercentileFallback)};

export const DYNO_INTEL_HALL_OF_FAME_SENTENCE_ZH = ${JSON.stringify(hallOfFameSentence)};

export const DYNO_INTEL_HALL_OF_FAME_LEGAL_SHIELD_ZH = ${JSON.stringify(hallOfFameLegalShield)};

export const DYNO_INTEL_HUMAN_PRAISE_BY_DECADE = ${JSON.stringify(humanPraiseByDecade, null, 2)};
`;
  writeFileSync(praiseDataPath, body, "utf8");
}

function patchScaleMatrix() {
  let src = readFileSync(scaleMatrixPath, "utf8");

  if (!src.includes('from "./dynoIntelHumanPraise.data.js"')) {
    src = src.replace(
      /const MOCK_NEURO_MALE = "\[Mock Neuro Male\]";\nconst MOCK_NEURO_FEMALE = "\[Mock Neuro Female\]";\nconst MOCK_VOLUME_MALE = "\[Mock Volume Male\]";\nconst MOCK_VOLUME_FEMALE = "\[Mock Volume Female\]";\n/,
      'import { DYNO_INTEL_HUMAN_PRAISE_BY_DECADE } from "./dynoIntelHumanPraise.data.js";\n\n'
    );
  }

  if (!src.includes("DYNO_INTEL_HUMAN_PRAISE_BY_DECADE[decadeKey]")) {
    src = src.replace(
      /function scaleBucket\(\{ tierId, scoreRange, populationClass, summaryHuman \}\) \{[\s\S]*?\n\}/,
      `function scaleBucket({ decadeKey, tierId, scoreRange, populationClass, summaryHuman }) {
  const praise = DYNO_INTEL_HUMAN_PRAISE_BY_DECADE[decadeKey] ?? DYNO_INTEL_HUMAN_PRAISE_BY_DECADE["0"];
  const neuro = praise?.neuro ?? summaryHuman;
  const volume = praise?.volume ?? summaryHuman;
  return {
    tierId,
    scoreRange,
    populationClass: praise?.populationClass ?? populationClass,
    summaryHuman,
    neuro_male: neuro,
    neuro_female: neuro,
    volume_male: volume,
    volume_female: volume,
  };
}`
    );
  }

  src = src.replace(
    /"(\d+)": scaleBucket\(\{\n    decadeKey: "[^"]+",/g,
    '"$1": scaleBucket({\n    decadeKey: "$1",'
  );

  writeFileSync(scaleMatrixPath, src, "utf8");
}

const source = loadPraiseSource();
writePraiseDataModule(source);
patchScaleMatrix();
console.log("Synced dynoIntelHumanPraise.data.js and dynoIntelHumanScaleMatrix.js from dynoIntel.json");
