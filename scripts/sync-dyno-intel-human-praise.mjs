#!/usr/bin/env node
/**
 * Syncs zh-Hant + en dynoIntel.json (humanBrief + hallOfFame) → Functions runtime data layer.
 * Run: npm run dyno:sync-human-praise
 *
 * DESIGN INTENT: i18n JSON is the single source of truth; Functions mirror is generated.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const zhDynoIntelPath = join(root, "src/i18n/locales/zh-Hant/common/dynoIntel.json");
const enDynoIntelPath = join(root, "src/i18n/locales/en/common/dynoIntel.json");
const praiseDataPath = join(root, "functions/dynoIntel/dynoIntelHumanPraise.data.js");

/** Must stay aligned with scoreBandResolver decade keys and zh-Hant humanPraise.byDecade. */
const PRAISE_DECADE_KEYS = ["0", "40", "50", "60", "70", "80", "90", "100", "110", "120", "130", "140", "150"];
const PRAISE_SLOT_FIELDS = ["populationClass", "overall", "neuro", "volume"];

function assertHumanPraiseParity(zhDecades, enDecades) {
  for (const decade of PRAISE_DECADE_KEYS) {
    if (!zhDecades[decade]) {
      throw new Error(`zh-Hant dynoIntel.json missing dynoIntel.humanPraise.byDecade["${decade}"]`);
    }
    if (!enDecades[decade]) {
      throw new Error(`en dynoIntel.json missing dynoIntel.humanPraise.byDecade["${decade}"]`);
    }
    for (const field of PRAISE_SLOT_FIELDS) {
      for (const [label, bucket] of [
        ["zh-Hant", zhDecades],
        ["en", enDecades],
      ]) {
        const value = String(bucket[decade][field] ?? "").trim();
        if (!value) {
          throw new Error(`${label} humanPraise.byDecade["${decade}"].${field} is empty`);
        }
        if (label === "en" && /[\u4e00-\u9fff]/.test(value)) {
          throw new Error(`en humanPraise.byDecade["${decade}"].${field} contains CJK`);
        }
      }
    }
  }
}

function loadLocaleBundle(localePath, localeLabel) {
  const doc = JSON.parse(readFileSync(localePath, "utf8"));
  const humanBrief = doc?.dynoIntel?.humanBrief;
  const hallOfFame = doc?.dynoIntel?.hallOfFame;
  const humanPraise = doc?.dynoIntel?.humanPraise?.byDecade;

  if (!humanBrief?.prPercentileFallback || !humanBrief?.hallOfFameSentence) {
    throw new Error(
      `${localeLabel} dynoIntel.json missing dynoIntel.humanBrief (prPercentileFallback + hallOfFameSentence)`
    );
  }
  if (!hallOfFame?.legalShield || typeof hallOfFame.legalShield !== "string") {
    throw new Error(`${localeLabel} dynoIntel.json missing dynoIntel.hallOfFame.legalShield`);
  }
  if (!hallOfFame?.blockedReply || typeof hallOfFame.blockedReply !== "string") {
    throw new Error(`${localeLabel} dynoIntel.json missing dynoIntel.hallOfFame.blockedReply`);
  }
  if (!hallOfFame?.guidedReply || typeof hallOfFame.guidedReply !== "string") {
    throw new Error(`${localeLabel} dynoIntel.json missing dynoIntel.hallOfFame.guidedReply`);
  }

  return {
    prPercentileFallback: humanBrief.prPercentileFallback,
    hallOfFameSentence: humanBrief.hallOfFameSentence,
    hallOfFameLegalShield: hallOfFame.legalShield,
    hallOfFameBlockedReply: hallOfFame.blockedReply,
    hallOfFameGuidedReply: hallOfFame.guidedReply,
    humanPraiseByDecade: humanPraise ?? null,
  };
}

function writePraiseDataModule({ zh, en }) {
  if (!zh.humanPraiseByDecade || typeof zh.humanPraiseByDecade !== "object") {
    throw new Error("zh-Hant dynoIntel.json missing dynoIntel.humanPraise.byDecade");
  }
  if (!en.humanPraiseByDecade || typeof en.humanPraiseByDecade !== "object") {
    throw new Error("en dynoIntel.json missing dynoIntel.humanPraise.byDecade");
  }

  const body = `/**
 * AUTO-GENERATED by scripts/sync-dyno-intel-human-praise.mjs — do not edit by hand.
 * Sources:
 * - src/i18n/locales/zh-Hant/common/dynoIntel.json
 * - src/i18n/locales/en/common/dynoIntel.json
 */
export const DYNO_INTEL_PR_PERCENTILE_FALLBACK_ZH = ${JSON.stringify(zh.prPercentileFallback)};

export const DYNO_INTEL_PR_PERCENTILE_FALLBACK_EN = ${JSON.stringify(en.prPercentileFallback)};

export const DYNO_INTEL_HALL_OF_FAME_SENTENCE_ZH = ${JSON.stringify(zh.hallOfFameSentence)};

export const DYNO_INTEL_HALL_OF_FAME_SENTENCE_EN = ${JSON.stringify(en.hallOfFameSentence)};

export const DYNO_INTEL_HALL_OF_FAME_LEGAL_SHIELD_ZH = ${JSON.stringify(zh.hallOfFameLegalShield)};

export const DYNO_INTEL_HALL_OF_FAME_LEGAL_SHIELD_EN = ${JSON.stringify(en.hallOfFameLegalShield)};

export const DYNO_INTEL_HALL_OF_FAME_BLOCKED_REPLY_ZH = ${JSON.stringify(zh.hallOfFameBlockedReply)};

export const DYNO_INTEL_HALL_OF_FAME_BLOCKED_REPLY_EN = ${JSON.stringify(en.hallOfFameBlockedReply)};

export const DYNO_INTEL_HALL_OF_FAME_GUIDED_REPLY_ZH = ${JSON.stringify(zh.hallOfFameGuidedReply)};

export const DYNO_INTEL_HALL_OF_FAME_GUIDED_REPLY_EN = ${JSON.stringify(en.hallOfFameGuidedReply)};

export const DYNO_INTEL_HUMAN_PRAISE_BY_DECADE = ${JSON.stringify(zh.humanPraiseByDecade, null, 2)};

export const DYNO_INTEL_HUMAN_PRAISE_BY_DECADE_EN = ${JSON.stringify(en.humanPraiseByDecade, null, 2)};
`;
  writeFileSync(praiseDataPath, body, "utf8");
}

const zh = loadLocaleBundle(zhDynoIntelPath, "zh-Hant");
const en = loadLocaleBundle(enDynoIntelPath, "en");
assertHumanPraiseParity(zh.humanPraiseByDecade, en.humanPraiseByDecade);
writePraiseDataModule({ zh, en });
console.log(
  "Synced dynoIntelHumanPraise.data.js from zh-Hant + en dynoIntel.json (ZH/EN dual-track constants)"
);
